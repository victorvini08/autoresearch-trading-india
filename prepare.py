# Skip feeds that don't have enough bars for the strategy's longest indicator
# warmup (50-day SMA + small safety margin). With multi-feed strategies,
# loading short-history tickers crashes backtrader's vectorized indicator
# pass with an IndexError. Single-feed didn't expose this because Cerebro
# only built indicators on the first feed.
_MIN_BARS_PER_FEED = 60


def _load_feeds(
    start: date, end: date, tickers: list[str]
) -> dict[str, pd.DataFrame]:
    """Load OHLCV from local DuckDB. Tickers with fewer than _MIN_BARS_PER_FEED
    rows in [start, end] are silently skipped (recent IPOs, mid-window adds,
    delistings).

    Raises only if NO tickers survive, which indicates a setup problem.
    """
    feeds: dict[str, pd.DataFrame] = {}
    for tkr in tickers:
        df = read_prices(tkr, start.isoformat(), end.isoformat())
        if len(df) < _MIN_BARS_PER_FEED:
            continue
        df = df.set_index(pd.DatetimeIndex(df["date"])).drop(columns=["date"])
        feeds[tkr] = df
    if not feeds:
        raise RuntimeError(
            f"No price data with >= {_MIN_BARS_PER_FEED} bars in [{start}, {end}]. "
            "Run data.ingest_prices first."
        )
    return feeds


